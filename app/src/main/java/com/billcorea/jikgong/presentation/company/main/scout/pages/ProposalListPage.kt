@Composable
fun ProposalListPage(
    modifier: Modifier = Modifier,
    proposals: List<Proposal>,
    isLoading: Boolean,
    onProposalClick: (Proposal) -> Unit,
    onRefresh: () -> Unit
) {
    // ÏÉÅÌÉúÎ≥Ñ Ï†úÏïà Î∂ÑÎ•ò
    val acceptedProposals = proposals.filter { it.status == ProposalStatus.ACCEPTED }
    val pendingProposals = proposals.filter { it.status == ProposalStatus.PENDING }
    val rejectedProposals = proposals.filter { it.status == ProposalStatus.REJECTED }

    if (isLoading && proposals.isEmpty()) {
        // Ï¥àÍ∏∞ Î°úÎî©
        Box(
            modifier = modifier.fillMaxSize(),
            contentAlignment = Alignment.Center
        ) {
            CircularProgressIndicator(
                color = Color(0xFF4B7BFF)
            )
        }
    } else if (proposals.isEmpty()) {
        // Îπà ÏÉÅÌÉú
        Box(
            modifier = modifier.fillMaxSize()
        ) {
            EmptyState(
                title = "ÏïÑÏßÅ Ï†úÏïàÌïú ÎÇ¥Ïó≠Ïù¥ ÏóÜÏäµÎãàÎã§",
                description = "Ïù∏Î†• Î™©Î°ùÏóêÏÑú ÌïÑÏöîÌïú Ïù∏Î†•ÏùÑ\nÏä§Ïπ¥ÏõÉÌï¥Î≥¥ÏÑ∏Ïöî",
                icon = "üìã"
            )
        }
    } else {
        // Ï†úÏïà Î™©Î°ù
        LazyColumn(
            modifier = modifier.fillMaxSize(),
            contentPadding = PaddingValues(16.dp),
            verticalArrangement = Arrangement.spacedBy(12.dp)
        ) {
            // ÏÉàÎ°úÍ≥†Ïπ® Î∞è Î°úÎî© ÌëúÏãú
            item {
                if (isLoading) {
                    LinearProgressIndicator(
                        modifier = Modifier
                            .fillMaxWidth()
                            .padding(bottom = 8.dp),
                        color = Color(0xFF4B7BFF)
                    )
                }
            }

            // ÌÜµÍ≥Ñ Ïπ¥Îìú
            item {
                ProposalStatisticsCard(
                    totalCount = proposals.size,
                    acceptedCount = acceptedProposals.size,
                    pendingCount = pendingProposals.size,
                    rejectedCount = rejectedProposals.size,
                    onRefresh = onRefresh,
                    isLoading = isLoading
                )
            }

            // ÏàòÎùΩÎêú Ï†úÏïà
            if (acceptedProposals.isNotEmpty()) {
                item {
                    SectionHeader(
                        title = "ÏàòÎùΩÎê®",
                        count = acceptedProposals.size,
                        color = Color(0xFF66BB6A)
                    )
                }

                items(acceptedProposals) { proposal ->
                    ProposalCard(
                        proposal = proposal,
                        onCardClick = { onProposalClick(proposal) }
                    )
                }
            }

            // ÎåÄÍ∏∞Ï§ëÏù∏ Ï†úÏïà
            if (pendingProposals.isNotEmpty()) {
                item {
                    SectionHeader(
                        title = "ÎåÄÍ∏∞Ï§ë",
                        count = pendingProposals.size,
                        color = Color(0xFFFFA726)
                    )
                }

                items(pendingProposals) { proposal ->
                    ProposalCard(
                        proposal = proposal,
                        onCardClick = { onProposalClick(proposal) }
                    )
                }
            }

            // Í±∞Ï†àÎêú Ï†úÏïà
            if (rejectedProposals.isNotEmpty()) {
                item {
                    SectionHeader(
                        title = "Í±∞Ï†àÎê®",
                        count = rejectedProposals.size,
                        color = Color(0xFFEF5350)
                    )
                }

                items(rejectedProposals) { proposal ->
                    ProposalCard(
                        proposal = proposal,
                        onCardClick = { onProposalClick(proposal) }
                    )
                }
            }

            // ÌïòÎã® Ïó¨Î∞±
            item {
                Spacer(modifier = Modifier.height(80.dp))
            }
        }
    }
}

@Composable
private fun ProposalStatisticsCard(
    totalCount: Int,
    acceptedCount: Int,
    pendingCount: Int,
    rejectedCount: Int,
    onRefresh: () -> Unit,
    isLoading: Boolean
) {
    Card(
        modifier = Modifier.fillMaxWidth(),
        colors = CardDefaults.cardColors(
            containerColor = Color.White
        ),
        elevation = CardDefaults.cardElevation(defaultElevation = 2.dp)
    ) {
        Column(
            modifier = Modifier.padding(16.dp),
            verticalArrangement = Arrangement.spacedBy(12.dp)
        ) {
            Row(
                modifier = Modifier.fillMaxWidth(),
                horizontalArrangement = Arrangement.SpaceBetween,
                verticalAlignment = Alignment.CenterVertically
            ) {
                Text(
                    text = "Ï†úÏïà ÌòÑÌô©",
                    style = MaterialTheme.typography.titleMedium.copy(
                        fontWeight = FontWeight.Bold
                    )
                )

                // ÏÉàÎ°úÍ≥†Ïπ® Î≤ÑÌäº
                IconButton(
                    onClick = onRefresh,
                    enabled = !isLoading,
                    modifier = Modifier.size(32.dp)
                ) {
                    Icon(
                        imageVector = Icons.Default.Refresh,
                        contentDescription = "ÏÉàÎ°úÍ≥†Ïπ®",
                        tint = if (isLoading) Color.Gray else Color(0xFF4B7BFF)
                    )
                }
            }

            Row(
                modifier = Modifier.fillMaxWidth(),
                horizontalArrangement = Arrangement.SpaceEvenly
            ) {
                StatisticItem(
                    label = "Ï†ÑÏ≤¥",
                    value = totalCount.toString(),
                    color = Color.Black
                )
                StatisticItem(
                    label = "ÏàòÎùΩ",
                    value = acceptedCount.toString(),
                    color = Color(0xFF66BB6A)
                )
                StatisticItem(
                    label = "ÎåÄÍ∏∞",
                    value = pendingCount.toString(),
                    color = Color(0xFFFFA726)
                )
                StatisticItem(
                    label = "Í±∞Ï†à",
                    value = rejectedCount.toString(),
                    color = Color(0xFFEF5350)
                )
            }

            // ÏàòÎùΩÎ•†
            if (totalCount > 0) {
                val acceptanceRate = (acceptedCount.toFloat() / totalCount * 100).toInt()
                LinearProgressIndicator(
                    progress = { acceptedCount.toFloat() / totalCount },
                    modifier = Modifier.fillMaxWidth(),
                    color = Color(0xFF66BB6A),
                    trackColor = Color(0xFFE0E0E0)
                )
                Text(
                    text = "ÏàòÎùΩÎ•† $acceptanceRate%",
                    style = MaterialTheme.typography.bodySmall,
                    color = Color.Gray
                )
            }
        }
    }
}

@Composable
private fun StatisticItem(
    label: String,
    value: String,
    color: Color
) {
    Column(
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
        Text(
            text = value,
            style = MaterialTheme.typography.titleLarge.copy(
                fontWeight = FontWeight.Bold
            ),
            color = color
        )
        Text(
            text = label,
            style = MaterialTheme.typography.bodySmall,
            color = Color.Gray
        )
    }
}

@Composable
private fun SectionHeader(
    title: String,
    count: Int,
    color: Color
) {
    Row(
        modifier = Modifier
            .fillMaxWidth()
            .padding(vertical = 8.dp),
        horizontalArrangement = Arrangement.SpaceBetween,
        verticalAlignment = Alignment.CenterVertically
    ) {
        Row(
            horizontalArrangement = Arrangement.spacedBy(8.dp),
            verticalAlignment = Alignment.CenterVertically
        ) {
            Surface(
                shape = MaterialTheme.shapes.small,
                color = color.copy(alpha = 0.1f)
            ) {
                Text(
                    text = title,
                    modifier = Modifier.padding(horizontal = 12.dp, vertical = 4.dp),
                    style = MaterialTheme.typography.bodyMedium.copy(
                        fontWeight = FontWeight.Bold
                    ),
                    color = color
                )
            }
            Text(
                text = "${count}Í±¥",
                style = MaterialTheme.typography.bodyMedium,
                color = Color.Gray
            )
        }
    }
}

